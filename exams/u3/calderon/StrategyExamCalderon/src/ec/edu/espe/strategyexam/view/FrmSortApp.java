    private void SortNumbers() throws NumberFormatException {
        
        ArrayList<Integer> arr = new ArrayList<>();
        String[] input = txtInsert.getText().split(",");
        int[] insertedData = new int[input.length];
        for (int i = 0; i < input.length; i++) {
            insertedData[i] = Integer.parseInt(input[i].trim());
        }
        
        SortingContext sortcontext = new SortingContext();
        int[] sortedNumbers = sortcontext.sort(insertedData);
        
        txtOrdened.setText(Arrays.toString(sortedNumbers));
        
        String sortStrategy = sortcontext.getSortStrategy().getClass().getSimpleName();
        
        for (int i = 0; i < sortedNumbers.length; i++) {
            arr.add(sortedNumbers[i]);
        }
        
        Document document = new Document().append("_id", new ObjectId())
                .append("unsortedList", txtInsert.getText())
                .append("size", insertedData.length)
                .append("sortMethod", sortStrategy)
                .append("sortedList", arr.toString());

        SortedMethod.CreateSortedMethod(document);
    }

