
package ec.edu.espe.testaplicationstrategy.model;

/**
 *
 * @author Diego Casignia, Techware, DCCO-ESPE
 */
public class Result {
    private String unsorted;
    private int size;
    private String sortAlgorithym;
    private String sorted;
    /**
     * @return the unsorted
     */
    public String getUnsorted() {
        return unsorted;
    }

    /**
     * @param unsorted the unsorted to set
     */
    public void setUnsorted(String unsorted) {
        this.unsorted = unsorted;
    }

    /**
     * @return the size
     */
    public int getSize() {
        return size;
    }

    /**
     * @param size the size to set
     */
    public void setSize(int size) {
        this.size = size;
    }

    /**
     * @return the sortAlgorithym
     */
    public String getSortAlgorithym() {
        return sortAlgorithym;
    }

    /**
     * @param sortAlgorithym the sortAlgorithym to set
     */
    public void setSortAlgorithym(String sortAlgorithym) {
        this.sortAlgorithym = sortAlgorithym;
    }

    /**
     * @return the sorted
     */
    public String getSorted() {
        return sorted;
    }

    /**
     * @param sorted the sorted to set
     */
    public void setSorted(String sorted) {
        this.sorted = sorted;
    }
    
}
