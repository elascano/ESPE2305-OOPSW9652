    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        int[] data = convertStringToArray(txtSort.getText());
        SortingContext sc = new SortingContext();
        int sortedList[] = sc.sort(data);
        txtSortResult.setText(Arrays.toString(sortedList));

        String method = getUsedSortingMethod(sc.getSs());
        lblSortAlgorithm.setText("Sorting method used: " + method);

        Document sortDocument = new Document("Sort Algorithm", Arrays.toString(data)) 
                .append("unsorted", Arrays.toString(sortedList))
                .append("method", method);
        mongoDBConnection.getCollection().insertOne(sortDocument);

        JOptionPane.showMessageDialog(rootPane, "Data saved", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSortActionPerformed

