    private void btnButtonWindowsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnButtonWindowsActionPerformed
        int sys = 0; // Cambia esto según tu elección (0 para WinFactory)

        GUIFactory factory = GUIFactory.getFactory(sys);
        Button winButton = factory.createButton(); // Crear un botón utilizando WinFactory

        // Actualizar el valor del atributo caption con el contenido de txtName
        winButton.caption = txtName.getText();

        if (winButton instanceof WinButton) {
            winButton.paint();

            // Crear una ventana emergente con el mensaje formateado
            String message = "Im a WinButton: " + winButton.caption;
            JOptionPane.showMessageDialog(this, message);
        }

        // Asignar la instancia del botón actual al campo de clase
        currentButton = winButton;
    }//GEN-LAST:event_btnButtonWindowsActionPerformed

