    private void btnClerkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClerkActionPerformed
        title = "Clerk: ";
        readData();
        Clerk clerk = new Clerk(title, name);
        emptyField();
        txaEmployee.setText(txaEmployee.getText() + clerk.getName() + " " + clerk.getTitle() + "\n");

    }//GEN-LAST:event_btnClerkActionPerformed


    private void btnTellerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTellerActionPerformed
        title = "Teller: ";
        readData();
        Teller teller = new Teller(title, name);
        emptyField();
        teller.stateName();
        txaEmployee.append(teller.getName() + " " + teller.getTitle() + "\n");
    }//GEN-LAST:event_btnTellerActionPerformed

