    private void connectToDatabase(String databaseType) {
        Factory factory;
        factory = new FactoryOne();
      
        if (databaseType.equals("Oracle")){
            Connection connection = factory.createConnection("Oracle");
            JOptionPane.showMessageDialog(null, "You are connecting with " + connection.description());
        }else if (databaseType.equals("MySQL")) {
            Connection connection = factory.createConnection("MySQL");
            JOptionPane.showMessageDialog(null, "You are connecting with " + connection.description());
        }
    }
    
