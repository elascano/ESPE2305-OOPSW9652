    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        contact = readData();
        //JOptionPane.showMessageDialog(rootPane, "You are saving "+contact.getFullName());
        //int option = JOptionPane.showConfirmDialog(this, "Are you sure to save \n"+contact.getFullName(),"Saving contacts",2,JOptionPane.ERROR_MESSAGE);
        int option = JOptionPane.showConfirmDialog(this, "Are you sure to save \n"+contact.toString());
        switch(option){
            case 0:{
                JOptionPane.showMessageDialog(rootPane, "Saved");
                emptyFields();
                break;
            }
            case 1:{
                JOptionPane.showMessageDialog(rootPane, "Aborted");
                emptyFields();
                break;
            }
            case 2:{
                JOptionPane.showMessageDialog(rootPane, "Cancel");
                break;
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private Contact readData() throws NumberFormatException {
        Contact contact;
        int id;
        String fullName;
        String contactName;
        String phoneNumber;
        String email;
        String gender;
        String type;
        Date bornOnDate = new Date();
        ArrayList<String> hobbies = new ArrayList<>();
        String comments;
        String favoriteTeam;
        id = Integer.parseInt(txtId.getText());
        fullName = txtFullName.getText();
        contactName = cmbContactName.getSelectedItem().toString();
        phoneNumber = cmbPhoneNumber.getSelectedItem().toString();
        gender = cmbGender.getSelectedItem().toString();
        type = uliType.getSelectedValue().toString();
        email = txtEmail.getText();
        comments = txaComments.getText();
        favoriteTeam = txtFavoriteTeam.getText();
        contact = new Contact(id, fullName, contactName, phoneNumber, email, gender, type, bornOnDate, hobbies, comments, favoriteTeam);
        return contact;
    }

