    public void loadKeyringData() {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Color");
        model.addColumn("Price");

        String uri = "mongodb+srv://gcalvache:gcalvache@cluster0.qsalyjy.mongodb.net/?retryWrites=true&w=majority";
        try (MongoClient mongoClient = MongoClients.create(uri)) {
            MongoDatabase database = mongoClient.getDatabase("Keyring");
            MongoCollection<Document> collection = database.getCollection("Keyring");

            FindIterable<Document> iterable = collection.find();
            for (Document document : iterable) {
                int id = document.getInteger("Id");
                String color = document.getString("Color");
                float price = document.getDouble("Price").floatValue();

                model.addRow(new Object[]{id, color, price});
            }
        }

        tblKeyringData.setModel(model);

        tblKeyringData.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                if (!event.getValueIsAdjusting()) {
                    int selectedRow = tblKeyringData.getSelectedRow();
                    if (selectedRow != -1) {

                        btnEdit.setEnabled(true);
                    } else {

                        btnEdit.setEnabled(false);
                    }
                }
            }
        });
    }

