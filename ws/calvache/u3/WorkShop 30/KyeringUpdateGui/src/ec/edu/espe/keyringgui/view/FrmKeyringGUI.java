    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int id = Integer.parseInt(txtId.getText());
        Document query = new Document("Id", id);
        Document document = mongoDBConnection.getCollection().find(query).first();

        if (document != null) {
            String newModelValue = txtColor.getText();
            float newPriceValue = Float.parseFloat(txtPrice.getText());

            Document updateDocument = new Document("$set", new Document("Color", newModelValue)
                    .append("Price", newPriceValue));
            mongoDBConnection.getCollection().updateOne(query, updateDocument);

            txtColor.setEditable(false);
            txtPrice.setEditable(false);

            this.dispose();
            EventQueue.invokeLater(() -> {
                FrmKeyringGUI newProductWindow = new FrmKeyringGUI();
                newProductWindow.setVisible(true);
            });
        }
    }//GEN-LAST:event_btnSaveActionPerformed
