    private Contact readData() throws NumberFormatException {
        Contact contact;
        int id;
        String fullName;
        String contactName;
        String phoneNumber;
        String email;
        String gender;
        String type = "";
        String bornOnDate;
        String hobbies;
        String comments;
        String favoriteTeam;
        
        id = Integer.parseInt(txtId.getText());
        fullName = txtFullName.getText();
        contactName = cmbContactName.getSelectedItem().toString();
        phoneNumber = txtPhoneNumber.getText();
        email = txtEmail.getText();
        comments = txaComments.getText();
        favoriteTeam = txtFavoriteTeam.getText();
        bornOnDate = txtBornOnDate.getText();
        hobbies = txtHobbies.getText();
        gender = cmbGender.getItemAt(cmbGender.getItemCount());
        
        contact = new Contact(id, fullName, contactName, phoneNumber, email, gender, type, bornOnDate, hobbies, comments, favoriteTeam);
        return contact;
    }

