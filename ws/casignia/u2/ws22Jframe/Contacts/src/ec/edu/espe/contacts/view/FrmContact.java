    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        contact = readData();
        int option = JOptionPane.showConfirmDialog(rootPane, "Are you sure to save\n");
        
        if (option == 0){
            JOptionPane.showMessageDialog(rootPane, "Save" + contact.toString());
            empltyFields();
        }
        if (option == 1){
            JOptionPane.showMessageDialog(rootPane, "Abort");
            empltyFields();
        }
        if (option == 3){
            JOptionPane.showMessageDialog(rootPane, "Cancel");
        }
    }//GEN-LAST:event_btnAddActionPerformed

